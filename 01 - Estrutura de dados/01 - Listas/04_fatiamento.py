lista = ["caminhão", "carro", "barco", "avião", "moto", "bicicleta"]

print(lista[2:])  # ['barco', 'avião', 'moto', 'bicicleta']
print(lista[:2])  # ['caminhão', 'carro']
print(lista[1:3])  # ['carro', 'barco']
print(lista[0:3:2])  # ['caminhão', 'barco']
print(lista[::])  # ['caminhão', 'carro', 'barco', 'avião', 'moto', 'bicicleta']
print(lista[::-1])  # ['bicicleta', 'moto', 'avião', 'barco', 'carro', 'caminhão']

